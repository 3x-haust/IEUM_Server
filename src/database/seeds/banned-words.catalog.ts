const prefixes = ['', '개', '씹', '존나', 'ㅈㄴ', 'ㅅㅂ'] as const;
const suffixes = ['', '아', '야', '요', '임', '같', '같음', '같네', '하네', '하다', '하냐', '새끼'] as const;

export function buildSeedBannedWords(): readonly string[] {
  const words: string[] = [];
  const seen = new Set<string>();
  const add = (word: string): void => {
    const trimmed = word.trim();
    const normalized = normalizeText(trimmed);
    if (trimmed && normalized && !seen.has(normalized) && normalized.length <= 100) {
      seen.add(normalized);
      words.push(trimmed);
    }
  };

  for (const word of [...publicKoreanBannedWords, ...directBannedWords, ...violenceAndSexualTerms, ...hateSpeechTerms]) {
    add(word);
  }
  for (const stem of [...feedbackAbuseStems, ...profanityStems]) {
    for (const prefix of prefixes) {
      for (const suffix of suffixes) {
        add(`${prefix}${stem}${suffix}`);
      }
    }
  }

  return words;
}
